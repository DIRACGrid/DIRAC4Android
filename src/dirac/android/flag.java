package dirac.android;

public class flag {
	private String verified;
	private String deleted;
	private String retrieved;
	private String outputSandboxReady;
	private String inputSandboxReady;
	private String accounted;
	private String killed;

	public String getVerified() {
		return verified;
	}
	public void setVerified(String verified) {
		this.verified = verified;
	}
	 
	public String getDeleted() {
		return deleted;
	}
	public void setDeleted(String deleted) {
		this.deleted = deleted;
	}
	public String getRetrieved() {
		return retrieved;
	}
	public void setRetrieved(String retrieved) {
		this.retrieved = retrieved;
	}

	public String getOutputSandboxReady() {
		return outputSandboxReady;
	}
	public void setOutputSandboxReady(String outputSandboxReady) {
		this.outputSandboxReady = outputSandboxReady;
	}

	public String getInputSandboxReady() {
		return inputSandboxReady;
	}
	public void setInputSandboxReady(String inputSandboxReady) {
		this.inputSandboxReady = inputSandboxReady;
	}
	

	public String getAccounted() {
		return accounted;
	}
	public void setAccounted(String accounted) {
		this.accounted = accounted;
	}
	

	public String getKilled() {
		return killed;
	}
	public void setKilled(String killed) {
		this.killed = killed;
	}
	
	

}
